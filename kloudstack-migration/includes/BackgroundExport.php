    /**
     * ZIP the given source directory and upload to Azure Blob via SAS URL.
     *
     * Special value 'custom-root': exports only root-level files from WP_CONTENT_DIR,
     * excluding standard sub-directories (plugins, themes, uploads, mu-plugins,
     * languages, upgrade).
     *
     * @param string $job_id      Job transient key
     * @param string $sas_url     Azure Blob SAS PUT URL
     * @param string $source_path Absolute path, or 'custom-root' sentinel
     * @param array  $hints       Optional skip_extensions / max_file_size_mb
     * @throws Exception on filesystem or upload failure
     */
    private static function _run_content_export( string $job_id, string $sas_url, string $source_path, array $hints = [] ): bool {
        self::_validate_sas_url( $sas_url );

        if ( ! class_exists( 'ZipArchive' ) ) {
            throw new Exception( 'PHP ZipArchive extension is not available.' );
        }

        $skip_extensions = isset( $hints['skip_extensions'] ) && is_array( $hints['skip_extensions'] )
            ? array_map( 'strtolower', $hints['skip_extensions'] )
            : [];
        $max_size_bytes  = isset( $hints['max_file_size_mb'] ) && $hints['max_file_size_mb'] > 0
            ? ( (int) $hints['max_file_size_mb'] ) * 1024 * 1024
            : 0;

        $is_custom_root = ( 'custom-root' === $source_path );
        $base_dir       = $is_custom_root ? WP_CONTENT_DIR : $source_path;

        if ( ! is_dir( $base_dir ) ) {
            throw new Exception( "Source directory does not exist: {$base_dir}" );
        }

        // Sub-directories to exclude when scanning WP_CONTENT_DIR root.
        $excluded_subdirs = [ 'plugins', 'themes', 'uploads', 'mu-plugins', 'languages', 'upgrade' ];

        // WordPress drop-in files are environment-specific: they contain connection
        // credentials, paths, and configuration for the SOURCE server.  Copying them
        // to the target stack causes PHP fatal errors (e.g. W3TC's db.php calling
        // wp_get_wp_version() before WordPress has finished bootstrapping).
        // Exclude them here — caching plugins will regenerate correct drop-ins for
        // the target environment when they are re-enabled after migration.
        $excluded_root_files = [
            'db.php',
            'advanced-cache.php',
            'object-cache.php',
            'db-error.php',
            'maintenance.php',
        ];

        $tmp_zip = tempnam( sys_get_temp_dir(), 'ks_content_' ) . '.zip';

        try {
            $zip = new ZipArchive();
            if ( true !== $zip->open( $tmp_zip, ZipArchive::CREATE | ZipArchive::OVERWRITE ) ) {
                throw new Exception( "Cannot create ZIP archive at {$tmp_zip}" );
            }

            $file_count = 0;

            if ( $is_custom_root ) {
                // Only root-level files in WP_CONTENT_DIR; skip standard sub-directories.
                $dir_iter = new DirectoryIterator( $base_dir );
                foreach ( $dir_iter as $file ) {
                    if ( $file->isDot() ) {
                        continue;
                    }
                    if ( $file->isDir() ) {
                        if ( in_array( $file->getFilename(), $excluded_subdirs, true ) ) {
                            continue;
                        }
                        // Non-standard sub-directory: include recursively.
                        $sub_iter = new RecursiveIteratorIterator(
                            new RecursiveDirectoryIterator( $file->getPathname(), FilesystemIterator::SKIP_DOTS ),
                            RecursiveIteratorIterator::LEAVES_ONLY
                        );
                        foreach ( $sub_iter as $sub_file ) {
                            if ( ! $sub_file->isFile() ) {
                                continue;
                            }
                            if ( $skip_extensions ) {
                                $ext = strtolower( pathinfo( $sub_file->getFilename(), PATHINFO_EXTENSION ) );
                                if ( in_array( '.' . $ext, $skip_extensions, true ) || in_array( $ext, $skip_extensions, true ) ) {
                                    continue;
                                }
                            }
                            if ( $max_size_bytes > 0 && $sub_file->getSize() > $max_size_bytes ) {
                                continue;
                            }
                            $relative = str_replace( $base_dir . DIRECTORY_SEPARATOR, '', $sub_file->getPathname() );
                            $zip->addFile( $sub_file->getPathname(), $relative );
                            $file_count++;
                        }
                        continue;
                    }
                    // Root-level file.
                    if ( in_array( $file->getFilename(), $excluded_root_files, true ) ) {
                        continue;
                    }
                    if ( $skip_extensions ) {
                        $ext = strtolower( pathinfo( $file->getFilename(), PATHINFO_EXTENSION ) );
                        if ( in_array( '.' . $ext, $skip_extensions, true ) || in_array( $ext, $skip_extensions, true ) ) {
                            continue;
                        }
                    }
                    if ( $max_size_bytes > 0 && $file->getSize() > $max_size_bytes ) {
                        continue;
                    }
                    $zip->addFile( $file->getPathname(), $file->getFilename() );
                    $file_count++;
                }
            } else {
                $iterator = new RecursiveIteratorIterator(
                    new RecursiveDirectoryIterator( $base_dir, FilesystemIterator::SKIP_DOTS ),
                    RecursiveIteratorIterator::LEAVES_ONLY
                );
                foreach ( $iterator as $file ) {
                    if ( ! $file->isFile() ) {
                        continue;
                    }
                    if ( $skip_extensions ) {
                        $ext = strtolower( pathinfo( $file->getFilename(), PATHINFO_EXTENSION ) );
                        if ( in_array( '.' . $ext, $skip_extensions, true ) || in_array( $ext, $skip_extensions, true ) ) {
                            continue;
                        }
                    }
                    if ( $max_size_bytes > 0 && $file->getSize() > $max_size_bytes ) {
                        continue;
                    }
                    $relative = str_replace( $base_dir . DIRECTORY_SEPARATOR, '', $file->getPathname() );
                    $zip->addFile( $file->getPathname(), $relative );
                    $file_count++;
                    if ( 0 === $file_count % 100 ) {
                        self::_update_job( $job_id, [ 'progress' => min( 55, 5 + (int) ( $file_count / 20 ) ) ] );
                    }
                }
            }

            $zip->close();

            if ( ! file_exists( $tmp_zip ) || 0 === filesize( $tmp_zip ) ) {
                throw new Exception( 'ZIP archive creation produced empty output.' );
            }

            self::_update_job( $job_id, [ 'progress' => 65 ] );
            self::_upload_file_to_blob( $sas_url, $tmp_zip, 'application/zip' );

            return true;

        } finally {
            if ( file_exists( $tmp_zip ) ) {
                unlink( $tmp_zip );
            }
        }
    }

