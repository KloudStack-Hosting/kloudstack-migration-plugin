    /**
     * Create a ZIP of wp-content/uploads and upload to Azure Blob.
     * Kept for any jobs queued before Sprint M. New jobs use _run_media_stream().
     *
     * @param string $job_id
     * @param string $sas_url  Must be HTTPS
     * @return bool
     * @throws Exception
     */
    private static function _run_media_upload( string $job_id, string $sas_url, array $hints = [] ): bool {
        self::_validate_sas_url( $sas_url );

        if ( ! class_exists( 'ZipArchive' ) ) {
            throw new Exception( 'PHP ZipArchive extension is not available.' );
        }

        $uploads_dir = wp_upload_dir();
        $base_dir    = $uploads_dir['basedir'];

        if ( ! is_dir( $base_dir ) ) {
            throw new Exception( "Uploads directory not found: {$base_dir}" );
        }

        // Agent hints: skip files by extension or size on retry attempts.
        // These are applied when a previous attempt timed out due to large video/archive files.
        $skip_extensions   = isset( $hints['skip_extensions'] ) && is_array( $hints['skip_extensions'] )
            ? array_map( 'strtolower', $hints['skip_extensions'] )
            : [];
        $max_size_bytes = isset( $hints['max_file_size_mb'] ) && $hints['max_file_size_mb'] > 0
            ? ( (int) $hints['max_file_size_mb'] ) * 1024 * 1024
            : 0;  // 0 = no limit

        $tmp_zip = tempnam( sys_get_temp_dir(), 'ks_media_' ) . '.zip';

        try {
            $zip = new ZipArchive();
            if ( true !== $zip->open( $tmp_zip, ZipArchive::CREATE | ZipArchive::OVERWRITE ) ) {
                throw new Exception( "Cannot create ZIP archive at {$tmp_zip}" );
            }

            $iterator = new RecursiveIteratorIterator(
                new RecursiveDirectoryIterator( $base_dir, FilesystemIterator::SKIP_DOTS ),
                RecursiveIteratorIterator::LEAVES_ONLY
            );

            $file_count = 0;
            foreach ( $iterator as $file ) {
                if ( $file->isFile() ) {
                    // Apply agent hints: skip by extension and/or file size.
                    if ( $skip_extensions ) {
                        $ext = strtolower( pathinfo( $file->getFilename(), PATHINFO_EXTENSION ) );
                        if ( in_array( '.' . $ext, $skip_extensions, true ) || in_array( $ext, $skip_extensions, true ) ) {
                            continue;
                        }
                    }
                    if ( $max_size_bytes > 0 && $file->getSize() > $max_size_bytes ) {
                        continue;
                    }

                    $relative_path = str_replace( $base_dir . DIRECTORY_SEPARATOR, '', $file->getPathname() );
                    $zip->addFile( $file->getPathname(), $relative_path );
                    $file_count++;

                    // Update progress periodically during the zip phase (5–55%).
                    // ZipArchive::addFile() only stores the path reference — actual
                    // file data is compressed at close() time, so no memory flush
                    // is needed here.
                    if ( 0 === $file_count % 100 ) {
                        self::_update_job( $job_id, [ 'progress' => min( 55, 5 + (int) ( $file_count / 20 ) ) ] );
                    }
                }
            }

            $zip->close();

            if ( ! file_exists( $tmp_zip ) || 0 === filesize( $tmp_zip ) ) {
                throw new Exception( 'ZIP archive creation produced empty output.' );
            }

            self::_update_job( $job_id, [ 'progress' => 65 ] );

            // Upload to Azure Blob via SAS PUT
            self::_upload_file_to_blob( $sas_url, $tmp_zip, 'application/zip' );

            return true;

        } finally {
            if ( file_exists( $tmp_zip ) ) {
                unlink( $tmp_zip );
            }
        }
    }

