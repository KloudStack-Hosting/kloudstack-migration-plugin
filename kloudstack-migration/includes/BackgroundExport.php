    private static function _update_job( string $job_id, array $updates ): void {
        global $wpdb;
        $transient_key  = KloudStack_Migration_RestEndpoints::JOB_TRANSIENT_PREFIX . $job_id;
        $option_name    = '_transient_' . $transient_key;

        // Primary path: normal transient read/write (works outside shutdown context).
        $job = get_transient( $transient_key );

        if ( false === $job ) {
            // Fallback for post-fastcgi_finish_request shutdown context: WordPress's own
            // shutdown function fires do_action('shutdown') — which closes the object cache
            // (W3TC, APCu, Redis) — BEFORE our shutdown function runs (PHP FIFO order).
            // Also fires when another PHP-FPM worker created the transient via a
            // process-local cache (APCu), making it invisible to this worker's get_transient().
            // RestEndpoints::_shadow_write_transient() guarantees the row exists in wp_options.
            $db_val = $wpdb->get_var( $wpdb->prepare(
                "SELECT option_value FROM {$wpdb->options} WHERE option_name = %s LIMIT 1",
                $option_name
            ) );
            if ( null === $db_val ) {
                error_log( '[KS Migration] _update_job DB-fallback: ROW MISSING for ' . $job_id . ' (option=' . $option_name . ') wpdb_err=' . $wpdb->last_error );
                return; // Genuinely missing — nothing to update.
            }
            $job = maybe_unserialize( $db_val );
            if ( ! is_array( $job ) ) {
                error_log( '[KS Migration] _update_job DB-fallback: unserialize failed for ' . $job_id );
                return;
            }
            $job = array_merge( $job, $updates );
            // Use REPLACE (INSERT OR UPDATE) instead of UPDATE so the write succeeds even
            // when the MySQL user lacks UPDATE privilege — observed on some managed hosts
            // where db_write_roundtrip (which uses replace) passes but update returns 0.
            $rows = $wpdb->replace(
                $wpdb->options,
                [
                    'option_name'  => $option_name,
                    'option_value' => maybe_serialize( $job ),
                    'autoload'     => 'no',
                ]
            );
            error_log( '[KS Migration] _update_job DB-fallback: ' . $job_id . ' rows_affected=' . $rows . ' progress=' . ( $updates['progress'] ?? '?' ) . ' status=' . ( $updates['status'] ?? '?' ) . ' wpdb_err=' . $wpdb->last_error );
            // Write audit entry accessible via /job-debug so we can see the DB write
            // result remotely without needing access to the WordPress debug.log file.
            $wpdb->replace( $wpdb->options, [
                'option_name'  => 'ks_mig_audit_' . substr( $job_id, -12 ),
                'option_value' => json_encode( [
                    'job_id'  => $job_id,
                    'path'    => 'db_fallback',
                    'updates' => $updates,
                    'rows'    => $rows,
                    'err'     => $wpdb->last_error ?: null,
                    'ts'      => time(),
                ] ),
                'autoload'     => 'no',
            ] );
            // Bust any stale in-memory option cache so the next get_transient() / get_option()
            // reads the fresh DB value rather than a cached 0% snapshot.
            wp_cache_delete( $option_name, 'options' );
            return;
        }

        $job = array_merge( $job, $updates );
        set_transient( $transient_key, $job, KloudStack_Migration_RestEndpoints::JOB_TTL );
        // Mirror into wp_options using REPLACE so the write succeeds even when the MySQL
        // user lacks UPDATE privilege (same fix as the DB-fallback path above).
        $wpdb->replace(
            $wpdb->options,
            [
                'option_name'  => $option_name,
                'option_value' => maybe_serialize( $job ),
                'autoload'     => 'no',
            ]
        );
        // Audit entry for primary path too (helps distinguish which path ran in logs).
        $wpdb->replace( $wpdb->options, [
            'option_name'  => 'ks_mig_audit_' . substr( $job_id, -12 ),
            'option_value' => json_encode( [
                'job_id'  => $job_id,
                'path'    => 'primary',
                'updates' => $updates,
                'ts'      => time(),
            ] ),
            'autoload'     => 'no',
        ] );
    }

    private static function _job_status( string $job_id ): string {
        global $wpdb;
        $transient_key = KloudStack_Migration_RestEndpoints::JOB_TRANSIENT_PREFIX . $job_id;
        $job           = get_transient( $transient_key );
        if ( false === $job ) {
            // Same shutdown-context fallback as _update_job.
            $db_val = $wpdb->get_var( $wpdb->prepare(
                "SELECT option_value FROM {$wpdb->options} WHERE option_name = %s LIMIT 1",
                '_transient_' . $transient_key
            ) );
            if ( null === $db_val ) {
                return 'unknown';
            }
            $job = maybe_unserialize( $db_val );
        }
        return is_array( $job ) ? ( $job['status'] ?? 'unknown' ) : 'unknown';
    }

    // ------------------------------------------------------------------
    // Media checkpoint helpers — direct wp_options read/write, no cache
    // ------------------------------------------------------------------

    /**
     * Return the wp_options key used to store the media upload checkpoint.
     * Kept short to stay well under the 191-byte index limit on option_name.
     */
    private static function _ckpt_option_key( string $job_id ): string {
        return 'ks_mig_ckpt_' . $job_id;
    }

    /**
     * Read the media upload checkpoint using a raw mysqli query, bypassing both the
     * WordPress object cache AND W3TC's database cache hooks entirely.
     *
     * Root cause of the v1.8.0/v1.8.1 stall: W3TC database cache (backed by APCu when
     * Redis is unavailable) caches the initial NULL result for the ks_mig_ckpt_ SELECT.
     * When $wpdb->replace() writes the checkpoint, W3TC should invalidate the APCu cache
     * entry, but the invalidation silently fails when Redis is unreachable.  Every
     * subsequent $wpdb->get_var() call then hits the stale NULL cache, returning [] for
     * uploaded_files so each batch re-uploads the same 500 files.  Using raw mysqli_query
     * on $wpdb->dbh bypasses $wpdb->query() entirely, removing all WordPress/W3TC hooks.
     *
     * @return array{ uploaded_files: string[], bytes_uploaded: int }
     */
    private static function _read_media_checkpoint( string $job_id ): array {
        global $wpdb;
        $key    = self::_ckpt_option_key( $job_id );
        $sql    = $wpdb->prepare(
            "SELECT option_value FROM {$wpdb->options} WHERE option_name = %s LIMIT 1",
            $key
        );
        $result = mysqli_query( $wpdb->dbh, $sql );
        $raw    = false;
        if ( $result ) {
            $row = mysqli_fetch_row( $result );
            $raw = $row ? $row[0] : false;
            mysqli_free_result( $result );
        }
        if ( ! $raw ) {
            $mysqli_err = mysqli_error( $wpdb->dbh ) ?: 'none';
            error_log( '[KS Migration] _read_media_checkpoint: NO ROW job=' . $job_id . ' key=' . $key . ' mysqli_err=' . $mysqli_err );
            return [ 'uploaded_files' => [], 'bytes_uploaded' => 0 ];
        }
        $data = maybe_unserialize( $raw );
        if ( ! is_array( $data ) ) {
            error_log( '[KS Migration] _read_media_checkpoint: UNSERIALIZE FAILED job=' . $job_id . ' raw_len=' . strlen( $raw ) );
            return [ 'uploaded_files' => [], 'bytes_uploaded' => 0 ];
        }
        $files_count = isset( $data['uploaded_files'] ) && is_array( $data['uploaded_files'] ) ? count( $data['uploaded_files'] ) : 0;
        error_log( '[KS Migration] _read_media_checkpoint: OK job=' . $job_id . ' files=' . $files_count . ' bytes=' . ( $data['bytes_uploaded'] ?? 0 ) );
        return [
            'uploaded_files' => is_array( $data['uploaded_files'] ?? null ) ? $data['uploaded_files'] : [],
            'bytes_uploaded' => (int) ( $data['bytes_uploaded'] ?? 0 ),
        ];
    }

    /**
     * Persist the media upload checkpoint using a raw mysqli query, bypassing both the
     * WordPress object cache AND W3TC's database cache hooks (same reason as _read).
     */
    private static function _write_media_checkpoint( string $job_id, array $uploaded, int $bytes_uploaded ): void {
        global $wpdb;
        $key   = self::_ckpt_option_key( $job_id );
        $value = maybe_serialize( [
            'uploaded_files' => $uploaded,
            'bytes_uploaded' => $bytes_uploaded,
        ] );
        $sql   = $wpdb->prepare(
            "INSERT INTO {$wpdb->options} (option_name, option_value, autoload) VALUES (%s, %s, 'no') ON DUPLICATE KEY UPDATE option_value = VALUES(option_value), autoload = 'no'",
            $key,
            $value
        );
        $ok    = (bool) mysqli_query( $wpdb->dbh, $sql );
        $rows  = $ok ? (int) mysqli_affected_rows( $wpdb->dbh ) : 0;
        $err   = $ok ? 'none' : ( mysqli_error( $wpdb->dbh ) ?: 'unknown' );
        error_log( '[KS Migration] _write_media_checkpoint: job=' . $job_id . ' files=' . count( $uploaded ) . ' bytes=' . $bytes_uploaded . ' rows=' . $rows . ' mysqli_err=' . $err );
    }

    /**
     * Delete the media upload checkpoint from wp_options on successful job completion.
     */
    private static function _delete_media_checkpoint( string $job_id ): void {
        global $wpdb;
        $sql = $wpdb->prepare(
            "DELETE FROM {$wpdb->options} WHERE option_name = %s",
            self::_ckpt_option_key( $job_id )
        );
        mysqli_query( $wpdb->dbh, $sql );
    }

    /**
     * Validate that a SAS URL is HTTPS to prevent SSRF via HTTP downgrade.
     *
     * @param string $sas_url
     * @throws Exception if URL is not HTTPS
     */
    private static function _validate_sas_url( string $sas_url ): void {
        if ( empty( $sas_url ) ) {
            throw new Exception( 'SAS URL is empty.' );
        }
        if ( 0 !== strpos( $sas_url, 'https://' ) ) {
            throw new Exception( 'SAS URL must use HTTPS.' );
        }
        // Basic host check — must point to .blob.core.windows.net
        $host = parse_url( $sas_url, PHP_URL_HOST );
        if ( false === strpos( $host, '.blob.core.windows.net' ) ) {
            throw new Exception( "SAS URL host is not a trusted Azure Blob endpoint: {$host}" );
        }
    }
