<?php
/**
 * KloudStack Migration — Background Export Engine
 *
 * Manages async jobs for DB dump and per-file media upload to Azure Blob Storage.
 * Uses WP-Cron for scheduling: process_queue() runs every 60 seconds.
 *
 * Job lifecycle:
 *   queued → processing → complete | failed
 *
 * DB export:
 *   1. Shell-out to mysqldump --single-transaction --quick (respects WP DB constants)
 *   2. Write raw SQL to a temp file (no gzip — avoids CPU spike on shared hosting)
 *   3. Upload the SQL dump to Azure Blob via SAS PUT request
 *   4. Update job transient → complete
 *
 * Media upload (per-file streaming — Sprint M):
 *   1. Enumerate all files in wp-content/uploads/ recursively
 *   2. For each file: cURL PUT directly to Azure Blob using a container-level SAS token
 *      — no ZIP, no /tmp disk usage, no compression
 *   3. Track uploaded files in the job transient for resume-on-retry (checkpointed)
 *   4. After all files: upload a _manifest.json blob to signal completion
 *   5. Update job transient → complete
 *
 * Content export (plugins / themes / mu-plugins / custom-root):
 *   1. Create a ZIP archive of the source directory
 *   2. Stream upload to Azure Blob via SAS PUT request
 *   3. Update job transient → complete
 *
 * Security:
 *   - mysqldump credentials taken from WP_DB_* constants (never user input)
 *   - Command arguments individually escaped with escapeshellarg()
 *   - SAS URLs and SAS tokens validated to be HTTPS / Azure Blob endpoints before use
 *   - Temp files created in sys_get_temp_dir() with unique names and deleted after use
 *
 * @package KloudStackMigration
 */

if ( ! defined( 'ABSPATH' ) ) {
    exit;
}

class KloudStack_Migration_BackgroundExport {

    const CRON_HOOK    = 'kloudstack_process_export_queue';
    const QUEUE_OPTION = 'kloudstack_export_queue';

    // Max jobs processed per cron tick / shutdown call.
    // Set high enough to drain all content_export jobs (plugins + themes + mu-plugins)
    // in a single pass — critical for hosts with DISABLE_WP_CRON=true where only one
    // shutdown-function invocation fires and WP-Cron never picks up remaining jobs.
    const MAX_JOBS_PER_RUN = 10;

    // Chunk size for streaming upload: 4 MB
    const UPLOAD_CHUNK_SIZE = 4 * 1024 * 1024;

    // ------------------------------------------------------------------
    // Cron setup
    // ------------------------------------------------------------------

    public static function setup_cron(): void {
        if ( ! wp_next_scheduled( self::CRON_HOOK ) ) {
            wp_schedule_event( time(), 'every_minute', self::CRON_HOOK );
        }
        add_filter( 'cron_schedules', [ __CLASS__, 'add_cron_interval' ] );
        add_action( self::CRON_HOOK, [ __CLASS__, 'process_queue' ] );
    }

    public static function add_cron_interval( array $schedules ): array {
        if ( ! isset( $schedules['every_minute'] ) ) {
            $schedules['every_minute'] = [
                'interval' => 60,
                'display'  => 'Every Minute',
            ];
        }
        return $schedules;
    }

    // ------------------------------------------------------------------
    // Queue management
    // ------------------------------------------------------------------

    /**
     * Enqueue a new export job.
     *
     * @param string $job_id     Unique job ID (generated by RestEndpoints)
     * @param string $type       'db_export' | 'media_upload' | 'content_export'
     * @param string $sas_url    Azure Blob SAS URL for uploading
     * @param array  $extra_data Additional fields to store in the queue item (e.g. source_path, artifact)
     */
    public static function enqueue( string $job_id, string $type, string $sas_url, array $extra_data = [] ): void {
        $queue   = get_option( self::QUEUE_OPTION, [] );
        $queue[] = array_merge(
            [
                'job_id'  => $job_id,
                'type'    => $type,
                'sas_url' => $sas_url,
            ],
            $extra_data
        );
        update_option( self::QUEUE_OPTION, $queue, false );
    }

    /**
     * Process the next job(s) from the queue.
     * Called by WP-Cron every minute.
     */
    public static function process_queue(): void {
        global $wpdb;

        // DB-level mutex using INSERT IGNORE — works in shutdown context where
        // set_transient() / get_transient() fail because the object cache (APCu, W3TC,
        // Redis) is closed by WordPress's own shutdown hook before ours runs (PHP FIFO).
        // A transient-based lock is silently a no-op here, allowing concurrent execution.
        $lock_name = 'ks_mig_queue_lock';
        $now       = time();

        $acquired = (bool) $wpdb->query( $wpdb->prepare(
            "INSERT IGNORE INTO {$wpdb->options} (option_name, option_value, autoload) VALUES (%s, %s, 'no')",
            $lock_name,
            (string) $now
        ) );

        if ( ! $acquired ) {
            // Row exists — check if it is stale (process was killed before finally released it).
            $lock_ts = (int) $wpdb->get_var( $wpdb->prepare(
                "SELECT option_value FROM {$wpdb->options} WHERE option_name = %s LIMIT 1",
                $lock_name
            ) );
            if ( $lock_ts > 0 && ( $now - $lock_ts ) < 600 ) {
                return; // Fresh lock — another invocation is running.
            }
            // Stale lock (> 10 min) — force-claim it so a killed process cannot block forever.
            $wpdb->update( $wpdb->options, [ 'option_value' => (string) $now ], [ 'option_name' => $lock_name ] );
        }

        try {
            // Read the queue directly from DB, bypassing the WordPress object cache.
            //
            // get_option() returns from the per-process in-memory cache first.  When two
            // PHP-FPM workers handle concurrent REST requests (e.g. Worker A for
            // export-site-content, Worker B for upload-media), each worker's cache only
            // reflects its OWN update_option() calls.  Worker A's cache has [plugins, themes];
            // Worker B wrote [plugins, themes, media] to DB afterwards but Worker A's cache
            // never received that update.  Calling get_option() in Worker A's shutdown
            // therefore returns stale [plugins, themes], and the media job is silently wiped
            // when update_option( QUEUE_OPTION, [] ) is written at the end of the run.
            $raw   = $wpdb->get_var( $wpdb->prepare(
                "SELECT option_value FROM {$wpdb->options} WHERE option_name = %s LIMIT 1",
                self::QUEUE_OPTION
            ) );
            $queue = $raw ? maybe_unserialize( $raw ) : [];
            if ( ! is_array( $queue ) ) {
                $queue = [];
            }
            if ( empty( $queue ) ) {
                return;
            }

            $processed = 0;
            $remaining = [];
            // Track every job ID we read from the queue snapshot so we can detect
            // items added by concurrent requests (e.g. /upload-media) while we process.
            $original_ids = array_flip( array_column( $queue, 'job_id' ) );

            foreach ( $queue as $item ) {
                if ( $processed >= self::MAX_JOBS_PER_RUN ) {
                    $remaining[] = $item;
                    continue;
                }

                $job_id  = $item['job_id'];
                $type    = $item['type'];
                $sas_url = $item['sas_url'];

                // Load agent-provided hints stored when the job was queued.
                // DB fallback mirrors _update_job(): object cache may be closed in shutdown.
                $hints_key     = KloudStack_Migration_RestEndpoints::JOB_TRANSIENT_PREFIX . $job_id;
                $job_transient = get_transient( $hints_key );
                if ( false === $job_transient ) {
                    $db_val        = $wpdb->get_var( $wpdb->prepare(
                        "SELECT option_value FROM {$wpdb->options} WHERE option_name = %s LIMIT 1",
                        '_transient_' . $hints_key
                    ) );
                    $job_transient = $db_val ? maybe_unserialize( $db_val ) : null;
                }
                $hints = ( is_array( $job_transient ) && isset( $job_transient['hints'] ) && is_array( $job_transient['hints'] ) )
                    ? $job_transient['hints']
                    : [];

                // Mark as processing
                error_log( '[KS Migration] process_queue: starting job=' . $job_id . ' type=' . $type );
                self::_update_job( $job_id, [ 'status' => 'processing', 'progress' => 5 ] );

                $success = false;
                try {
                    if ( 'db_export' === $type ) {
                        $success = self::_run_db_export( $job_id, $sas_url, $hints );
                    } elseif ( 'media_stream' === $type ) {
                        $container_base_url = $item['container_base_url'] ?? '';
                        $blob_prefix        = $item['blob_prefix'] ?? '';
                        $sas_token          = $item['sas_token'] ?? '';
                        $success = self::_run_media_stream( $job_id, $container_base_url, $blob_prefix, $sas_token, $hints );
                    } elseif ( 'media_upload' === $type ) {
                        // Legacy job type — kept for any jobs queued before Sprint M.
                        $success = self::_run_media_upload( $job_id, $sas_url, $hints );
                    } elseif ( 'content_export' === $type ) {
                        $source_path = $item['source_path'] ?? '';
                        $success = self::_run_content_export( $job_id, $sas_url, $source_path, $hints );
                    } elseif ( 'content_stream' === $type ) {
                        $container_base_url = $item['container_base_url'] ?? '';
                        $blob_prefix        = $item['blob_prefix'] ?? '';
                        $sas_token          = $item['sas_token'] ?? '';
                        $source_path        = $item['source_path'] ?? '';
                        $artifact           = $item['artifact'] ?? '';
                        $success = self::_run_content_stream( $job_id, $container_base_url, $blob_prefix, $sas_token, $source_path, $artifact, $hints );
                    }
                } catch ( Exception $e ) {
                    self::_update_job( $job_id, [
                        'status' => 'failed',
                        'error'  => substr( $e->getMessage(), 0, 500 ),
                    ] );
                    error_log( '[KS Migration] process_queue: job ' . $job_id . ' EXCEPTION: ' . $e->getMessage() );
                }

                if ( $success ) {
                    self::_update_job( $job_id, [ 'status' => 'complete', 'progress' => 100 ] );
                } elseif ( 'failed' !== self::_job_status( $job_id ) ) {
                    $remaining[] = $item; // Re-queue if not explicitly marked failed
                }

                $processed++;
            }

            // Re-read the queue directly from DB (bypassing per-process cache — same
            // reason as the initial read above) to pick up items enqueued by a concurrent
            // request while we were processing.  We add only items not in our original
            // snapshot AND not already done.
            $raw_fresh   = $wpdb->get_var( $wpdb->prepare(
                "SELECT option_value FROM {$wpdb->options} WHERE option_name = %s LIMIT 1",
                self::QUEUE_OPTION
            ) );
            $fresh_queue = $raw_fresh ? maybe_unserialize( $raw_fresh ) : [];
            if ( ! is_array( $fresh_queue ) ) {
                $fresh_queue = [];
            }
            $new_items   = [];
            foreach ( $fresh_queue as $fresh_item ) {
                $fid = $fresh_item['job_id'] ?? '';
                if ( $fid && ! isset( $original_ids[ $fid ] ) ) {
                    $s = self::_job_status( $fid );
                    if ( 'complete' !== $s && 'failed' !== $s ) {
                        $new_items[] = $fresh_item;
                    }
                }
            }

            update_option( self::QUEUE_OPTION, array_values( array_merge( $remaining, $new_items ) ), false );
        } finally {
            $wpdb->delete( $wpdb->options, [ 'option_name' => $lock_name ] );
        }
    }

    /**
     * Drain the queue by running process_queue() back-to-back until the queue is
     * empty or the wall-clock budget is exhausted.
     *
     * process_queue() does ONE batch per job and returns, which — when invoked from
     * the background shutdown runner (set_time_limit(600) after fastcgi_finish_request)
     * — ends the long-lived background process after a single ~25 s batch and leaves
     * the rest of the work waiting for the next WP-Cron tick / server nudge (~60-90 s
     * apart). On a low-traffic source site that means large content streams crawl.
     *
     * Looping here keeps the SAME already-running background process busy for the full
     * window, so a big plugins/media export drains in minutes instead of hours. Each
     * inner batch still checkpoints, so a process killed early loses nothing.
     *
     * @param int $budget_seconds Stay safely under the runner's set_time_limit(600).
     */
    public static function drain_queue( int $budget_seconds = 550 ): void {
        $start = microtime( true );
        do {
            self::process_queue();

            // Read the queue straight from the DB (bypass per-process cache, same
            // reasoning as process_queue) to see whether incomplete work remains.
            global $wpdb;
            $raw      = $wpdb->get_var( $wpdb->prepare(
                "SELECT option_value FROM {$wpdb->options} WHERE option_name = %s LIMIT 1",
                self::QUEUE_OPTION
            ) );
            $pending  = $raw ? maybe_unserialize( $raw ) : [];
            $has_work = is_array( $pending ) && ! empty( $pending );

            // Small pause between batches so we never hot-spin (e.g. if a job is
            // momentarily between checkpoints) and to be gentle on the source DB.
            if ( $has_work ) {
                usleep( 250000 ); // 0.25 s
            }
        } while ( $has_work && ( microtime( true ) - $start ) < $budget_seconds );
    }

    // ------------------------------------------------------------------
    // DB Export
    // ------------------------------------------------------------------

    /**
     * Export the WordPress database to a plain SQL dump and upload to Azure Blob.
     *
     * Streams mysqldump output to a temp file without gzip compression.
     * --single-transaction avoids table locks on InnoDB (zero impact on the live site).
     * --quick reads one row at a time, keeping memory usage minimal on shared hosting.
     *
     * @param string $job_id
     * @param string $sas_url  Must be HTTPS
     * @return bool  true on success
     * @throws Exception  on mysqldump failure or upload failure
     */
    private static function _run_db_export( string $job_id, string $sas_url, array $hints = [] ): bool {
        self::_validate_sas_url( $sas_url );

        $disable_fns    = array_filter( array_map( 'trim', explode( ',', (string) ini_get( 'disable_functions' ) ) ) );
        $exec_available = function_exists( 'exec' ) && ! in_array( 'exec', $disable_fns, true );

        $rate_limit_kbps = isset( $hints['stream_rate_limit_kbps'] ) ? max( 128, (int) $hints['stream_rate_limit_kbps'] ) : 0;

        $tmp_file = tempnam( sys_get_temp_dir(), 'ks_db_' ) . '.sql';

        try {
            if ( $exec_available ) {
                self::_mysqldump_export( $tmp_file, $rate_limit_kbps );
            } else {
                self::_wpdb_export( $job_id, $tmp_file );
            }

            self::_update_job( $job_id, [ 'progress' => 70 ] );
            self::_upload_file_to_blob( $sas_url, $tmp_file, 'application/octet-stream' );
            self::_update_job( $job_id, [ 'progress' => 100 ] );
            return true;

        } finally {
            if ( file_exists( $tmp_file ) ) {
                @unlink( $tmp_file );
            }
            putenv( 'MYSQL_PWD=' );
        }
    }

    /**
     * Export via mysqldump — used when exec() is available (default path).
     * --single-transaction: InnoDB snapshot, zero table locking.
     * --quick: reads one row at a time, minimal RAM on restricted accounts.
     */
    private static function _mysqldump_export( string $tmp_file, int $rate_limit_kbps = 0 ): void {
        $host     = escapeshellarg( DB_HOST );
        $user     = escapeshellarg( DB_USER );
        $database = escapeshellarg( DB_NAME );
        $tmp_esc  = escapeshellarg( $tmp_file );

        putenv( 'MYSQL_PWD=' . DB_PASSWORD );

        if ( $rate_limit_kbps > 0 ) {
            $pv_available = ! empty( shell_exec( 'which pv 2>/dev/null' ) );
            if ( $pv_available ) {
                $rate_arg = escapeshellarg( $rate_limit_kbps . 'k' );
                $cmd = "bash -c 'mysqldump --host={$host} --user={$user} --single-transaction "
                     . "--quick --routines --triggers --add-drop-table {$database} "
                     . "| pv -q -L {$rate_arg} > {$tmp_esc} 2>/dev/null'";
            } else {
                $cmd = "bash -c 'mysqldump --host={$host} --user={$user} --single-transaction "
                     . "--quick --routines --triggers --add-drop-table {$database} "
                     . "> {$tmp_esc} 2>/dev/null'";
            }
        } else {
            $cmd = "bash -c 'mysqldump --host={$host} --user={$user} --single-transaction "
                 . "--quick --routines --triggers --add-drop-table {$database} "
                 . "> {$tmp_esc} 2>/dev/null'";
        }

        $output    = [];
        $exit_code = 0;
        exec( $cmd, $output, $exit_code );

        putenv( 'MYSQL_PWD=' );

        if ( 0 !== $exit_code ) {
            throw new Exception( 'mysqldump failed (exit=' . $exit_code . '): ' . implode( ' ', $output ) );
        }
        if ( ! file_exists( $tmp_file ) || 0 === filesize( $tmp_file ) ) {
            throw new Exception( 'mysqldump produced empty output.' );
        }
    }

    /**
     * PHP-native DB export fallback for environments where exec() is disabled
     * (e.g. WP Engine, Kinsta, some shared hosting).
     *
     * Iterates all tables via SHOW TABLES, writes DDL via SHOW CREATE TABLE, and
     * dumps rows as batched INSERT statements (500 rows/batch) to avoid PHP memory
     * exhaustion on large tables.
     *
     * Output is compatible with the mysqldump import path used by run_db_import.
     * Progress updates run from 5% to 65%, leaving 70% for upload completion.
     */
    private static function _wpdb_export( string $job_id, string $tmp_file ): void {
        global $wpdb;

        $fh = fopen( $tmp_file, 'wb' );
        if ( ! $fh ) {
            throw new Exception( 'PHP-native DB export: failed to open temp file for writing.' );
        }

        try {
            fwrite( $fh, "-- KloudStack PHP-native export (exec() unavailable)\n" );
            fwrite( $fh, "-- Generated: " . gmdate( 'Y-m-d H:i:s' ) . " UTC\n\n" );
            fwrite( $fh, "SET NAMES utf8mb4;\n" );
            fwrite( $fh, "SET FOREIGN_KEY_CHECKS=0;\n\n" );

            $tables = $wpdb->get_col( 'SHOW TABLES' );
            $total  = count( $tables );
            $done   = 0;

            foreach ( $tables as $table ) {
                $safe = '`' . str_replace( '`', '``', $table ) . '`';

                // DDL
                // phpcs:ignore WordPress.DB.PreparedSQL.InterpolatedNotPrepared
                $create_row = $wpdb->get_row( "SHOW CREATE TABLE {$safe}", ARRAY_N );
                if ( $create_row ) {
                    fwrite( $fh, "DROP TABLE IF EXISTS {$safe};\n" );
                    fwrite( $fh, $create_row[1] . ";\n\n" );
                }

                // Row data — batched to avoid memory exhaustion on large tables
                $offset = 0;
                $batch  = 500;
                while ( true ) {
                    // phpcs:ignore WordPress.DB.PreparedSQL.InterpolatedNotPrepared
                    $rows = $wpdb->get_results(
                        $wpdb->prepare( "SELECT * FROM {$safe} LIMIT %d OFFSET %d", $batch, $offset ),
                        ARRAY_A
                    );
                    if ( empty( $rows ) ) {
                        break;
                    }

                    $col_names = implode( ', ', array_map(
                        static fn( $c ) => '`' . str_replace( '`', '``', $c ) . '`',
                        array_keys( $rows[0] )
                    ) );

                    $value_rows = [];
                    foreach ( $rows as $row ) {
                        $vals = array_map( static function ( $v ) {
                            return is_null( $v ) ? 'NULL' : "'" . esc_sql( (string) $v ) . "'";
                        }, array_values( $row ) );
                        $value_rows[] = '(' . implode( ', ', $vals ) . ')';
                    }

                    fwrite( $fh, "INSERT INTO {$safe} ({$col_names}) VALUES\n" );
                    fwrite( $fh, implode( ",\n", $value_rows ) . ";\n" );

                    $offset += $batch;
                    if ( count( $rows ) < $batch ) {
                        break;
                    }
                }

                fwrite( $fh, "\n" );
                $done++;
                self::_update_job( $job_id, [ 'progress' => 5 + (int) ( 60 * $done / max( 1, $total ) ) ] );
            }

            fwrite( $fh, "SET FOREIGN_KEY_CHECKS=1;\n" );

        } finally {
            fclose( $fh );
        }

        if ( ! file_exists( $tmp_file ) || 0 === filesize( $tmp_file ) ) {
            throw new Exception( 'PHP-native DB export produced empty output.' );
        }
    }

    // ------------------------------------------------------------------
    // Media Stream — per-file direct Blob PUT (Sprint M)
    // ------------------------------------------------------------------

    /**
     * Stream every file in wp-content/uploads/ directly to Azure Blob Storage.
     *
     * Each file is PUT individually using a container-level SAS token — no ZIP,
     * no temp disk usage. The job transient tracks uploaded files so that a
     * retry/WP-Cron resumption skips already-completed files (checkpointing).
     *
     * A _manifest.json blob is uploaded as the final step to signal completion.
     *
     * @param string $job_id
     * @param string $container_base_url  e.g. https://{account}.blob.core.windows.net/kloudstack-migrations
     * @param string $blob_prefix         e.g. migrations/{id}/media/uploads
     * @param string $sas_token           Container-level write SAS (no leading '?')
     * @param array  $hints               Agent-provided hints (skip_extensions, max_file_size_mb)
     * @return bool  true when all files uploaded and manifest written
     * @throws Exception on unrecoverable upload failure
     */
    private static function _run_media_stream(
        string $job_id,
        string $container_base_url,
        string $blob_prefix,
        string $sas_token,
        array $hints = []
    ): bool {
        global $wpdb;
        error_log( '[KS Migration] _run_media_stream: START job=' . $job_id . ' container=' . $container_base_url . ' prefix=' . $blob_prefix . ' sas_len=' . strlen( $sas_token ) );

        // Validate the container base URL points to Azure Blob Storage over HTTPS.
        if ( 0 !== strpos( $container_base_url, 'https://' ) ) {
            throw new Exception( 'container_base_url must use HTTPS.' );
        }
        if ( false === strpos( parse_url( $container_base_url, PHP_URL_HOST ) ?? '', '.blob.core.windows.net' ) ) {
            throw new Exception( 'container_base_url host is not a trusted Azure Blob endpoint.' );
        }

        $uploads_dir = wp_upload_dir();
        $base_dir    = $uploads_dir['basedir'];

        if ( ! is_dir( $base_dir ) ) {
            throw new Exception( "Uploads directory not found: {$base_dir}" );
        }

        // Agent hints.
        $skip_extensions = isset( $hints['skip_extensions'] ) && is_array( $hints['skip_extensions'] )
            ? array_map( 'strtolower', $hints['skip_extensions'] )
            : [];
        $max_size_bytes = isset( $hints['max_file_size_mb'] ) && $hints['max_file_size_mb'] > 0
            ? ( (int) $hints['max_file_size_mb'] ) * 1024 * 1024
            : 0;

        // Batch size: number of files processed per WP-Cron tick.
        // Keeps each execution under ~8 minutes on slow shared hosting
        // (500 files × ~100 ms/HTTPS PUT = ~50 s, well within safe limits).
        $batch_size = 500;

        // Load checkpoint — list of relative paths already uploaded this job.
        //
        // v1.8.0 root-cause fix: the checkpoint must NOT use get_transient() at any point.
        //
        // With ext_object_cache=True (APCu/Redis), WordPress stores transients exclusively
        // in the external cache and never writes them to wp_options.  Two separate failure
        // modes combine to permanently stall the job at exactly one batch (20% for 2500 files):
        //
        // 1. SHUTDOWN CONTEXT (first batch, fired from /upload_media REST shutdown function):
        //    do_action('shutdown') closes APCu/Redis BEFORE our callback runs (PHP FIFO).
        //    get_transient() returns false. The v1.7.9 DB fallback reads _transient_ks_...
        //    from wp_options, but with ext_object_cache=True that row was never written by
        //    set_transient() — so the fallback also returns null. uploaded_files=[].
        //
        // 2. WP-CRON TICKS (subsequent batches, new PHP process, cache open):
        //    get_transient() succeeds — but returns the *original* job state from creation
        //    time (uploaded_files=[]).  The shutdown-context _update_job() calls only wrote
        //    to wp_options via DB-fallback; they never updated the external cache.  So every
        //    WP-Cron tick reads the stale cached state and re-uploads the same 500 files.
        //
        // Fix: store the uploaded-files checkpoint in a dedicated wp_options key
        // (ks_mig_ckpt_<job_id>) using $wpdb->replace() for writes and $wpdb->get_var()
        // for reads.  This bypasses the entire WordPress transient/cache stack and works
        // identically in both shutdown context and normal WP-Cron execution.
        $ckpt           = self::_read_media_checkpoint( $job_id );
        $uploaded       = $ckpt['uploaded_files'];
        $bytes_uploaded = $ckpt['bytes_uploaded'];
        $uploaded_set   = array_flip( $uploaded ); // O(1) lookups
        error_log( '[KS Migration] _run_media_stream: checkpoint loaded job=' . $job_id . ' already_uploaded=' . count( $uploaded ) . ' bytes_uploaded=' . $bytes_uploaded );

        // Build the full file list (only built once; subsequent batches use transient offset).
        $all_files = [];
        $iterator  = new RecursiveIteratorIterator(
            new RecursiveDirectoryIterator( $base_dir, FilesystemIterator::SKIP_DOTS ),
            RecursiveIteratorIterator::LEAVES_ONLY
        );
        foreach ( $iterator as $file ) {
            if ( ! $file->isFile() ) {
                continue;
            }
            if ( $skip_extensions ) {
                $ext = strtolower( pathinfo( $file->getFilename(), PATHINFO_EXTENSION ) );
                if ( in_array( '.' . $ext, $skip_extensions, true ) || in_array( $ext, $skip_extensions, true ) ) {
                    continue;
                }
            }
            if ( $max_size_bytes > 0 && $file->getSize() > $max_size_bytes ) {
                continue;
            }
            $all_files[] = [
                'abs'  => $file->getPathname(),
                'rel'  => str_replace( $base_dir . DIRECTORY_SEPARATOR, '', $file->getPathname() ),
                'size' => $file->getSize(),
            ];
        }

        $total_files = count( $all_files );
        $total_bytes = array_sum( array_column( $all_files, 'size' ) );

        error_log( '[KS Migration] _run_media_stream: enumerated total_files=' . $total_files . ' total_bytes=' . $total_bytes . ' base_dir=' . $base_dir );

        if ( 0 === $total_files ) {
            error_log( '[KS Migration] _run_media_stream: no files found — marking complete immediately' );
        }

        // Update totals in transient so the polling loop can display accurate counts.
        self::_update_job( $job_id, [
            'total_files'  => $total_files,
            'total_bytes'  => $total_bytes,
        ] );

        $files_this_batch    = 0;
        // How often to persist the uploaded_files checkpoint. Progress (count/bytes) is
        // written after every file (lightweight); the full path list is expensive to
        // serialise so we batch it every N files to avoid O(N²) write traffic.
        $checkpoint_interval = 50;

        // Wall-clock guard: yield before the host's PHP max_execution_time so a
        // large library is never killed mid-batch before a checkpoint is written
        // (count-based batching alone can blow the time limit on slow uploads).
        $max_exec    = (int) ini_get( 'max_execution_time' );
        // Cap each batch at ~25 s so we checkpoint frequently and yield well before
        // any host execution limit. The background runner calls set_time_limit(600),
        // which makes max_execution_time report 600 even on hosts that won't actually
        // honour it — so an uncapped 0.8×600 budget would risk a mid-batch kill before
        // a checkpoint. drain_queue() loops these short batches back-to-back to use the
        // full background window.
        $time_budget = $max_exec > 0 ? max( 10, min( 25, (int) floor( $max_exec * 0.8 ) ) ) : 25;
        $start_time  = microtime( true );

        foreach ( $all_files as $entry ) {
            $rel_path = $entry['rel'];

            // Skip files already uploaded (checkpointing — safe to resume after PHP-FPM kill).
            if ( isset( $uploaded_set[ $rel_path ] ) ) {
                continue;
            }

            // Normalise path separators to forward-slash for the blob name.
            $blob_rel  = str_replace( DIRECTORY_SEPARATOR, '/', $rel_path );
            $blob_name = "{$blob_prefix}/{$blob_rel}";
            // URL-encode each path segment (preserve '/') so files with spaces or
            // special characters (e.g. brackets in PhpSpreadsheet fixtures such as
            // "[Content_Types].xml") produce a valid URL. cURL otherwise rejects the
            // unencoded illegal characters with "URL using bad/illegal format or
            // missing URL" and the whole content/media job fails. The blob is still
            // stored under its real (decoded) name — Azure decodes the URL path.
            $blob_path = implode( '/', array_map( 'rawurlencode', explode( '/', $blob_name ) ) );
            $blob_url  = "{$container_base_url}/{$blob_path}?{$sas_token}";

            // Detect MIME type for accurate Content-Type header.
            $mime_type = 'application/octet-stream';
            if ( function_exists( 'mime_content_type' ) ) {
                $detected = mime_content_type( $entry['abs'] );
                if ( $detected ) {
                    $mime_type = $detected;
                }
            }

            if ( 1 === $files_this_batch + 1 ) {
                error_log( '[KS Migration] _run_media_stream: uploading first file=' . $blob_rel . ' size=' . $entry['size'] );
            }
            self::_upload_blob_put( $blob_url, $entry['abs'], $mime_type );

            // Record as uploaded in checkpoint.
            $uploaded[]              = $rel_path;
            $uploaded_set[$rel_path] = true;
            $bytes_uploaded         += $entry['size'];
            $files_this_batch++;

            // Lightweight progress update after every file — no large arrays.
            // min(99) reserves 100% for the final manifest signal.
            // max(1) ensures progress is always > 0% once we've uploaded at least one file
            // so the Django stagnation timer never fires prematurely on large libraries.
            $progress = $total_files > 0
                ? max( 1, min( 99, (int) ( count( $uploaded ) / $total_files * 99 ) ) )
                : 99;
            self::_update_job( $job_id, [
                'progress'       => $progress,
                'files_uploaded' => count( $uploaded ),
                'bytes_uploaded' => $bytes_uploaded,
            ] );

            // Heavy checkpoint (uploaded_files path list) every N files so re-runs skip
            // already-uploaded files without writing a growing blob on every single PUT.
            // Written directly to wp_options (bypasses cache) — see _write_media_checkpoint().
            if ( 0 === $files_this_batch % $checkpoint_interval ) {
                self::_write_media_checkpoint( $job_id, $uploaded, $bytes_uploaded );
            }

            // Yield after the batch limit OR the time budget to allow WP-Cron to reschedule.
            if ( $files_this_batch >= $batch_size
                || ( microtime( true ) - $start_time ) >= $time_budget ) {
                // Flush the final checkpoint directly to wp_options before exiting so the
                // next cron tick resumes from where we stopped (bypasses external cache).
                self::_write_media_checkpoint( $job_id, $uploaded, $bytes_uploaded );
                self::_update_job( $job_id, [ 'status' => 'processing' ] );
                return false; // Returning false re-queues the item for the next cron tick.
            }
        }

        // All files uploaded — write _manifest.json as the completion signal.
        $manifest = json_encode( [
            'total_files'  => $total_files,
            'total_bytes'  => $total_bytes,
            'completed_at' => time(),
        ] );
        // Build the manifest blob URL — strip /uploads suffix from the prefix so
        // the manifest lands at the container root for that migration, not inside /uploads.
        $media_base   = preg_replace( '#/uploads$#', '', $blob_prefix );
        $manifest_url = "{$container_base_url}/{$media_base}/_manifest.json?{$sas_token}";

        $tmp_manifest = tempnam( sys_get_temp_dir(), 'ks_manifest_' ) . '.json';
        try {
            file_put_contents( $tmp_manifest, $manifest );
            self::_upload_blob_put( $manifest_url, $tmp_manifest, 'application/json' );
        } finally {
            if ( file_exists( $tmp_manifest ) ) {
                unlink( $tmp_manifest );
            }
        }

        self::_update_job( $job_id, [
            'progress'       => 100,
            'files_uploaded' => $total_files,
            'bytes_uploaded' => $total_bytes,
        ] );

        // Clean up the dedicated checkpoint row — job is complete.
        self::_delete_media_checkpoint( $job_id );

        return true;
    }

